     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $user=User::findorFail($id);
        if($user->active==0){
            $user->active=1;
        }
        else{
            $user->active=0;
        }
        $user->save();
        return redirect()->back()->with('alert','Xong');
    }

